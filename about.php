<p>PlasmaKashmir is a non-profit initiative to connect COVID-19 patients to plasma donors in J&amp;K. </p>



<div class="wp-block-columns">
<div class="wp-block-column" style="flex-basis:33.33%">
<h3>Who we are</h3>
</div>



<div class="wp-block-column" style="flex-basis:66.66%">
<p>We are a group of young, independent volunteers dedicated to fighting coronavirus by all means possible.</p>
</div>
</div>



<div class="wp-block-columns">
<div class="wp-block-column" style="flex-basis:33.33%">
<h3>What we do</h3>
</div>



<div class="wp-block-column" style="flex-basis:66.66%">
<p>We connect <i>plasma donors</i> to <i>COVID-19 patients</i> through this website.</p>
</div>
</div>



<div class="wp-block-columns">
<div class="wp-block-column" style="flex-basis:33.33%">
<h3>How it works (for patients)</h3>
</div>



<div class="wp-block-column" style="flex-basis:66.66%">
<p>After registering yourself on this website, you will be assigned a volunteer who will contact you for verification. And then the volunteer will provide you the details of the matched donor.</p>
</div>
</div>

<div class="wp-block-columns">
<div class="wp-block-column" style="flex-basis:33.33%">
<h3>How it works (for donors)</h3>
</div>

<div class="wp-block-column" style="flex-basis:66.66%">
<p>After registering yourself on this website, you will be assigned a volunteer who will stay in touch with you. You can text the volunteer anytime if you want to remove your details from the website, or if you are temporarily unavailable to donate.</p>
</div>
</div>

